#include<iostream>
using namespace std;

int main()
{
    int i = 0;
    int c;
    cout<<"Enter the Number of rows: ";
    cin>>c;
    while (i<=c)
    {
        /* code */
        int j=1;
        while (j<=c)
        {
            /* code */
            cout<<j<<"  ";
            j++;
        }
        cout<<"\n";
        i++;
    }

    
}